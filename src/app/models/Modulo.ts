export class Modulo{
  
  public _idModulo : number;
  public get idModulo() : number {
    return this._idModulo;
  }
  public set idModulo(v : number) {
    this._idModulo = v;
  }
  
  public _nombre : string;
  public get nombre() : string {
    return this._nombre;
  }
  public set nombre(v : string) {
    this._nombre = v;
  }
  
}